# Get test score from user 
score = eval(input('Enter test score ')) 
 
# You add code to Test if score is between 0-100 
 
if score <60: 
    print ('F') 
elif score <70: 
    print ('D') 
elif score <80: 
    print ('C') 
elif score <90:
    print ('B') 
else: 
    print ('A')

print ('done')  
